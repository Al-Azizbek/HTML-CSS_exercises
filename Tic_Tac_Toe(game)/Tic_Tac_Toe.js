var rowOne_colOne = document.querySelector("#row1_col1")

rowOne_colOne.addEventListener('click', function(){
	if (rowOne_colOne.textContent == '') {
		rowOne_colOne.textContent = 'X';
	}else if (rowOne_colOne.textContent == 'X') {
		rowOne_colOne.textContent='O';
	}else{
		rowOne_colOne.textContent = '';
	}
})

